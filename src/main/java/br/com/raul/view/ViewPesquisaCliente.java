    public void readJTable() {
        DefaultTableModel dtmClientes = (DefaultTableModel) jTableClientes.getModel();
        dtmClientes.setNumRows(0);
        ClienteDAO cdao = new ClienteDAO();

        for (Cliente c : cdao.read()) {
            dtmClientes.addRow(new Object[]{
              //  c.getCod(),
                c.getNome(),
                c.getTipopessoa(),
                c.getCpfCnpj(),
                c.getCep(),
                c.getEndereco(),
                c.getNumero(),
                c.getComplemento(),
                c.getBairro(),
                c.getEstado(),
                c.getCidade(),
                c.getEmail(),
                c.getTelefone(),
                c.getSituacao(),});
        }
    }

